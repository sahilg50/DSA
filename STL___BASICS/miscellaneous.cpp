#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    // if(0) evaluates to False and everything else to True;
    if (0)
        cout << "if(0)";
    if (-1)
        cout << "if(-1)" << endl;
    if (1)
        cout << "if(1)" << endl;

    // sqrt() is a function of cmath library
    cout << sqrt(10);

    return 0;
}